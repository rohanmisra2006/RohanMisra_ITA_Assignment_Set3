# ── CELL 12: Live Prediction Function ────────────────────────────────────────
# Test the model on any statement you write yourself.

def predict_statement(statement_text):
    cleaned   = clean_text(statement_text)
    tfidf_vec = tfidf.transform([cleaned])
    length    = np.array([[len(cleaned.split())]])
    history   = np.zeros((1, len(HISTORY_COLS)))   # unknown speaker history
    features  = sp.hstack([tfidf_vec, sp.csr_matrix(np.hstack([history, length]))])

    prediction   = rf_model.predict(features)[0]
    probability  = rf_model.predict_proba(features)[0]

    label = "CREDIBLE" if prediction == 1 else "MISINFORMATION"
    conf  = probability[prediction] * 100

    print(f"\nStatement : {statement_text}")
    print(f"Prediction: {label}")
    print(f"Confidence: {conf:.1f}%")
    print(f"  Misinfo probability : {probability[0]*100:.1f}%")
    print(f"  Credible probability: {probability[1]*100:.1f}%")


